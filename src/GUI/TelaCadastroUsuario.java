    private void btn_CadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_CadastrarMouseClicked
        String nome = txtNome.getText();
        String senha = String.valueOf(txtSenha.getPassword());
        String email = txtEmail.getText();
        
        UsuarioController cliente = new UsuarioController();
        PrincipalController principal = new PrincipalController();
        
        if(!principal.encaminhaInsercaoUsuario(nome,senha,email)){
            JOptionPane.showMessageDialog(null, "ERRO no cadastro.");
            dispose();
            abreTela(new TelaCadastroUsuario());
        }
        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
        dispose();
        abreTela(new TelaLogin());
    }//GEN-LAST:event_btn_CadastrarMouseClicked

